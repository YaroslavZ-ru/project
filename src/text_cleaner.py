"""src/text_cleaner.py -- очистка входных строк перед лемматизацией.

Что остается: русские буквы, латинские буквы, цифры, пробелы, внутренние дефисы.
Что удаляется: знаки препинания, спецсимволы, краевые/изолированные дефисы.

Пример:
    clean_text('  -Ключ-гаечный! (размер 12)  ')  # -> 'ключ-гаечный размер 12'
    clean_text('ключ - гаечный')                      # -> 'ключ гаечный'
"""

import re
import logging

logger = logging.getLogger("ai_terminator.text_cleaner")

# Паттерн: всё кроме русских, латинских, цифр, пробела и дефиса
_RE_NON_ALLOWED = re.compile(r"[^а-яёa-z0-9 \-]")
# Дефис, у которого слева или справа нет не-пробельного символа
_RE_ISOLATED_HYPHEN = re.compile(r"(?<![а-яёa-z])-|-(?![а-яёa-z])")
_RE_MULTI_SPACE = re.compile(r" +")


def clean_text(text: str) -> str:
    """Очистить строку для лемматизации.

    Сохраняет внутренние дефисы ('ключ-гаечный' -> 'ключ-гаечный').
    Удаляет изолированные дефисы ('ключ - гаечный' -> 'ключ гаечный').
    Разбить на слова -- задача lemmatize_phrase.

    Args:
        text: сырая строка (термин или подсказка).

    Returns:
        Очищенная строка или '' если вход невалиден.
    """
    # Шаг 1. Проверка типа
    if not isinstance(text, str):
        return ""

    # Шаг 2. Нижний регистр
    text = text.lower()

    # Шаг 3. Заменить недопустимые символы пробелом
    text = _RE_NON_ALLOWED.sub(" ", text)

    # Шаг 4. Схлопнуть пробелы
    text = _RE_MULTI_SPACE.sub(" ", text)

    # Шаг 5. Обрезать пробелы и дефисы с краёв
    text = text.strip(" -")

    # Шаг 6. Удалить изолированные дефисы (без буквы слева/справа)
    text = _RE_ISOLATED_HYPHEN.sub(" ", text)
    text = _RE_MULTI_SPACE.sub(" ", text).strip()

    # Шаг 7. Вернуть результат
    return text
