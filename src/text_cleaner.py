import re
import logging

logger = logging.getLogger("ai_terminator.text_cleaner")

# \u041f\u0430\u0442\u0442\u0435\u0440\u043d: \u0432\u0441\u0451 \u043a\u0440\u043e\u043c\u0435 \u0440\u0443\u0441\u0441\u043a\u0438\u0445, \u043b\u0430\u0442\u0438\u043d\u0441\u043a\u0438\u0445, \u0446\u0438\u0444\u0440, \u043f\u0440\u043e\u0431\u0435\u043b\u0430 \u0438 \u0434\u0435\u0444\u0438\u0441\u0430
_RE_NON_ALLOWED = re.compile(r'[^\u0430-\u044f\u0451a-z0-9 \-]')
# \u0414\u0435\u0444\u0438\u0441, \u0443 \u043a\u043e\u0442\u043e\u0440\u043e\u0433\u043e \u0441\u043b\u0435\u0432\u0430 \u0438\u043b\u0438 \u0441\u043f\u0440\u0430\u0432\u0430 \u043d\u0435\u0442 \u043d\u0435-\u043f\u0440\u043e\u0431\u0435\u043b\u044c\u043d\u043e\u0433\u043e \u0441\u0438\u043c\u0432\u043e\u043b\u0430
_RE_ISOLATED_HYPHEN = re.compile(r'(?<![\u0430-\u044f\u0451a-z])-|-(?![\u0430-\u044f\u0451a-z])')
_RE_MULTI_SPACE = re.compile(r' +')


def clean_text(text: str) -> str:
    """\u041e\u0447\u0438\u0441\u0442\u0438\u0442\u044c \u0441\u0442\u0440\u043e\u043a\u0443 \u0434\u043b\u044f \u043b\u0435\u043c\u043c\u0430\u0442\u0438\u0437\u0430\u0446\u0438\u0438.

    \u0421\u043e\u0445\u0440\u0430\u043d\u044f\u0435\u0442 \u0432\u043d\u0443\u0442\u0440\u0435\u043d\u043d\u0438\u0435 \u0434\u0435\u0444\u0438\u0441\u044b ('\u043a\u043b\u044e\u0447-\u0433\u0430\u0435\u0447\u043d\u044b\u0439' -> '\u043a\u043b\u044e\u0447-\u0433\u0430\u0435\u0447\u043d\u044b\u0439').
    \u0423\u0434\u0430\u043b\u044f\u0435\u0442 \u0438\u0437\u043e\u043b\u0438\u0440\u043e\u0432\u0430\u043d\u043d\u044b\u0435 \u0434\u0435\u0444\u0438\u0441\u044b ('\u043a\u043b\u044e\u0447 - \u0433\u0430\u0435\u0447\u043d\u044b\u0439' -> '\u043a\u043b\u044e\u0447 \u0433\u0430\u0435\u0447\u043d\u044b\u0439').
    \u0420\u0430\u0437\u0431\u0438\u0442\u044c \u043d\u0430 \u0441\u043b\u043e\u0432\u0430 -- \u0437\u0430\u0434\u0430\u0447\u0430 lemmatize_phrase.

    Args:
        text: \u0441\u044b\u0440\u0430\u044f \u0441\u0442\u0440\u043e\u043a\u0430 (\u0442\u0435\u0440\u043c\u0438\u043d \u0438\u043b\u0438 \u043f\u043e\u0434\u0441\u043a\u0430\u0437\u043a\u0430).

    Returns:
        \u041e\u0447\u0438\u0449\u0435\u043d\u043d\u0430\u044f \u0441\u0442\u0440\u043e\u043a\u0430 \u0438\u043b\u0438 '' \u0435\u0441\u043b\u0438 \u0432\u0445\u043e\u0434 \u043d\u0435\u0432\u0430\u043b\u0438\u0434\u0435\u043d.
    """
    # \u0428\u0430\u0433 1. \u041f\u0440\u043e\u0432\u0435\u0440\u043a\u0430 \u0442\u0438\u043f\u0430
    if not isinstance(text, str):
        return ""

    # \u0428\u0430\u0433 2. \u041d\u0438\u0436\u043d\u0438\u0439 \u0440\u0435\u0433\u0438\u0441\u0442\u0440
    text = text.lower()

    # \u0428\u0430\u0433 3. \u0417\u0430\u043c\u0435\u043d\u0438\u0442\u044c \u043d\u0435\u0434\u043e\u043f\u0443\u0441\u0442\u0438\u043c\u044b\u0435 \u0441\u0438\u043c\u0432\u043e\u043b\u044b \u043f\u0440\u043e\u0431\u0435\u043b\u043e\u043c
    text = _RE_NON_ALLOWED.sub(" ", text)

    # \u0428\u0430\u0433 4. \u0421\u0445\u043b\u043e\u043f\u043d\u0443\u0442\u044c \u043f\u0440\u043e\u0431\u0435\u043b\u044b
    text = _RE_MULTI_SPACE.sub(" ", text)

    # \u0428\u0430\u0433 5. \u041e\u0431\u0440\u0435\u0437\u0430\u0442\u044c \u043f\u0440\u043e\u0431\u0435\u043b\u044b \u0438 \u0434\u0435\u0444\u0438\u0441\u044b \u0441 \u043a\u0440\u0430\u0451\u0432
    text = text.strip(" -")

    # \u0428\u0430\u0433 6. \u0423\u0434\u0430\u043b\u0438\u0442\u044c \u0438\u0437\u043e\u043b\u0438\u0440\u043e\u0432\u0430\u043d\u043d\u044b\u0435 \u0434\u0435\u0444\u0438\u0441\u044b (\u0431\u0435\u0437 \u0431\u0443\u043a\u0432\u044b \u0441\u043b\u0435\u0432\u0430/\u0441\u043f\u0440\u0430\u0432\u0430)
    text = _RE_ISOLATED_HYPHEN.sub(" ", text)
    text = _RE_MULTI_SPACE.sub(" ", text).strip()

    # \u0428\u0430\u0433 7. \u0412\u0435\u0440\u043d\u0443\u0442\u044c \u0440\u0435\u0437\u0443\u043b\u044c\u0442\u0430\u0442
    return text