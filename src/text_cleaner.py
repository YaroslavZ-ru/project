    clean_text('\u043a\u043b\u044e\u0447 - \u0433\u0430\u0435\u0447\u043d\u044b\u0439')                      # -> '\u043a\u043b\u044e\u0447 \u0433\u0430\u0435\u0447\u043d\u044b\u0439'
